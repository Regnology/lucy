package net.regnology.lucy.web.rest.v1;

import net.regnology.lucy.domain.LibraryErrorLog;
import net.regnology.lucy.repository.LibraryErrorLogCustomRepository;
import net.regnology.lucy.service.LibraryErrorLogCustomService;
import net.regnology.lucy.service.LibraryErrorLogQueryCustomService;
import net.regnology.lucy.web.rest.LibraryErrorLogResource;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

/**
 * Custom REST controller for managing {@link LibraryErrorLog}
 * where changes are inserted that are different from the base class generated by the JHipster generator.<br>
 * API base path: {@code /api/v1}
 */
@RestController
@RequestMapping("/api/v1")
public class LibraryErrorLogCustomResource extends LibraryErrorLogResource {

    public LibraryErrorLogCustomResource(
        LibraryErrorLogCustomService libraryErrorLogService,
        LibraryErrorLogCustomRepository libraryErrorLogRepository,
        LibraryErrorLogQueryCustomService libraryErrorLogQueryService
    ) {
        super(libraryErrorLogService, libraryErrorLogRepository, libraryErrorLogQueryService);
    }
}
